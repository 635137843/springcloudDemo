/**
 * @ClassName ConfigApplication
 * @Description TODO
 * @Author xin
 * @Date 2020/4/7 23:05
 * @Version 1.0
 **/
@SpringBootApplication
@EnableConfigServer
@EnableEurekaClient
public class ConfigApplication {
    public static void main(String[] args) {
        SpringApplication.run(ConfigApplication.class,args);
    }
}
